#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr_b;

	if (n == 0)
		return ;
	ptr_b = (unsigned char *)s;
	while (n-- > 0)
	{
		*ptr_b++ = '\0';
	}
}
