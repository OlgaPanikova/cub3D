#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*res;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(src);
	res = (char *)malloc(sizeof(char) * (len + 1));
	if (res == NULL)
		return (0);
	while (src[i] != '\0')
	{
		res[i] = src[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
